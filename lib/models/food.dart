class Food {
  final String name;
  final String image;
  final String description;
  final double price;
  final String category;
  final String rating;
  final String id;
  final int quantity;

  Food({
    required this.name,
    required this.image,
    required this.description,
    required this.price,
    required this.category,
    required this.rating,
    required this.id,
    required this.quantity,
  });
}
